package nhvinh;

public class Car {
	
	String Model = "";
	String Color = "";
	String Brand = "";
	
	String getModel() {		
		
		return Model;
	}
	
	String getColor() {
		
		return Color;
	}
	
	String getBrand() {
		
		return Brand;
	}
	
	void setModel(String n) {
		// Set Model here
		Model=n;
	}
	
	void setColor(String d) {
		// Set Color here
		Color =d;
	}
	
	void setBrand(String h) {
		// Set Brand here
		Brand=h;
	}

}
